            <div id="collapseThree" class="card-body collapse" data-parent="#accordion">
                <p>In 2001, Titan launched kids’ watches brand, Dash. The brand saw poor performance and was discontinued in 2003.In 2004, the company entered into an agreement with Moet Hennessy Louis Vuitton Group, to service the latter's range of watches in India through its customer care centres.In 2005, Fastrack was positioned as an independent accessory brand targeting the urban youth. With the objective to become a fashion brand, Fastrack launched sunglasses in the same year and launched bags, belts and wallets in 2009. </p>
