                 <div id="collapseOne" class="card-body collapse" data-parent="#accordion">
                <p>Titan Company Limited was inaugurated on 26 July 1984 under the name Titan Watches Limited in Chennai.A plant was set up to manufacture quartz analog electronic watches in the State Industries Promotion Corporation of Tamil Nadu, Ltd. Industrial area at Hosur. In November 1986, Titan Company and Casio signed a MoU proposing to manufacture 2 million digital and analog-digital watches.In 1989, a satellite case plant was set up in Dehradun, Uttar Pradesh, Uttarakhand at present with a manufacturing capacity of 500,000 watch cases annually. </p>
